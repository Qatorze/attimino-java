package com.qatorze.attimino.enums;

/**
 * Enumération représentant les différents types d'images.
 * Chaque valeur correspond à une catégorie spécifique d'images pouvant être utilisées dans l'application.
 */
public enum ImageType {
    CITY,    // Représente une image liée à une ville.
    THEATER, // Représente une image liée à un théâtre.
    SHOW     // Représente une image liée à un spectacle ou un événement.
}
